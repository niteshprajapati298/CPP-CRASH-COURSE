#include<iostream>
using namespace std;

int main() {
    // Input for integer
    int marksInt;
    cout << "Enter your integer marks: ";
    cin >> marksInt;
    cout << "Nitesh's Integer Marks: " << marksInt << endl;

    // Input for Double 
    double marksDouble;
    cout << "Enter your double marks: ";
    cin >> marksDouble;
    cout << "Nitesh's Double Marks: " << marksDouble << endl;

    // Input for character
    char alphabet;
    cout << "Enter your alphabet: ";
    cin >> alphabet;
    cout << "Nitesh's Alphabet: " << alphabet << endl;

    // Input for boolean
    bool marksBool;
    cout << "Enter your boolean marks (0 or 1): ";
    cin >> marksBool;
    cout << "Nitesh's Boolean Marks: " << marksBool << endl;

    return 0;
}
